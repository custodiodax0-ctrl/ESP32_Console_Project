static void drawDigit(int x, int y, int digit) {
    if (digit == 1) {
        // Draw 3x27 digit 1 bitmap centered in segment position
        display.drawBitmap(x + 6, y + 1, image_segment_4_bits, 3, 27, 1);
    } else if (digit >= 0 && digit <= 9) {
        // Draw 15x29 segment digit bitmap
        display.drawBitmap(x, y, image_digits_15x29[digit], 15, 29, 1);
    }
}

