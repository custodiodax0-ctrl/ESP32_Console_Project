void drawDashboard_Design(void) {
    display.clearDisplay();

    DateTime now = getCurrentTime();
    int hr = now.hour();
    int min = now.minute();

    int d1 = (hr / 10) % 10;
    int d2 = hr % 10;
    int d3 = (min / 10) % 10;
    int d4 = min % 10;

    // Battery indicator
    if (getBatteryPercent() > 20) {
        display.drawBitmap(110, 2, image_Battery_indicator_bits, 15, 7, 1);
    } else {
        if ((millis() / 500) % 2 == 0) {
            display.drawBitmap(110, 2, image_Battery_indicator_bits, 15, 7, 1);
        }
    }

    // BT indicator
    if (isBluetoothEnabled()) {
        display.drawBitmap(3, 2, image_BT_indicator_bits, 7, 7, 1);
    }

    // Wifi indicator
    if (isWifiConnected()) {
        display.drawBitmap(14, 2, image_Wifi_indicator_bits, 8, 7, 1);
    }

    // Location & Nation
    display.drawBitmap(84, 15, image_Loc_province_bits, 17, 5, 1);
    display.drawBitmap(105, 15, image_Nation_bits, 17, 5, 1);

    // Weather condition bitmap (16x15)
    const char* cond = getWeatherConditionText();
    if (strstr(cond, "Rain") != NULL) {
        display.drawBitmap(84, 26, image_weather_rainy, 16, 15, 1);
    } else if (strstr(cond, "Storm") != NULL || strstr(cond, "Thunder") != NULL) {
        display.drawBitmap(84, 26, image_weather_thunder, 16, 15, 1);
    } else if (strstr(cond, "Cloud") != NULL) {
        display.drawBitmap(84, 26, image_weather_cloudy, 16, 15, 1);
    } else {
        display.drawBitmap(84, 26, image_weather_sunny, 16, 15, 1);
    }

    // Indicators
    display.drawBitmap(103, 31, image_Rain_indicator_bits, 17, 7, 1);
    display.drawBitmap(84, 43, image_Temp_bitmap_bits, 16, 15, 1);
    display.drawBitmap(103, 47, image_Humidity_indicator_bits, 17, 7, 1);

    // Dynamic 7-Segment Digits
    drawDigit(6, 27, d1);
    drawDigit(23, 27, d2);
    drawDigit(46, 27, d3);
    drawDigit(63, 27, d4);

    // Colon (blinks every second)
    if ((millis() / 500) % 2 == 0) {
        display.drawBitmap(40, 31, image_colon_bits, 4, 21, 1);
    }

    // Days & Date Bitmaps
    display.drawBitmap(59, 17, image_current_days_bits, 17, 5, 1);
    display.drawBitmap(8, 17, image_Date_Month_bits, 17, 5, 1);
    display.drawBitmap(37, 17, image_Date_day_bits, 11, 5, 1);

    // Single Merged Border Frame (124x51 at x=2, y=11)
    display.drawBitmap(2, 11, image_border_pixels, 124, 51, 1);

    display.display();
}

